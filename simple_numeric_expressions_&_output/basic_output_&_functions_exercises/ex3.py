'''
Numeric Conversion. Write a print statement to print the mixed fraction 3+5/8 
as a floating point number and as an integer.
'''

#Floating-point number
float_n = float(3 + 5/8)
print('Represented as a float:', float_n)

#Integer number
integer_n = int(3 + 5/8)
print('Represented as an integer:', integer_n)